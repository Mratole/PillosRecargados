    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed

        int id = 1;
        if (lista != null)
        {
            id = lista.size() + 1;
        }
        String nom = nombre.getText();
        int cantidad = 0;
        double peso = 0.0;
        if (jLabel3.getText().compareTo("Peso") == 0)
        {
            peso = Double.parseDouble(this.cantidad.getText());
        } else
        {
            cantidad = Integer.parseInt(this.cantidad.getText());
        }

        double pre = Double.parseDouble(precio.getText());
        int tipo;
        String t = jPe.getSelectedItem() + "";
        if (t.compareTo("Perecederos") == 0)
        {
            tipo = 1;
        } else
        {
            tipo = 2;
        }
        int id_Provedor = Integer.parseInt((String) ListaPr.getSelectedItem());

        ManipulaBD.AltasProductos(id, id_Provedor, nom, cantidad, pre, peso, tipo, true);
        llenarTabla();


    }//GEN-LAST:event_aceptarActionPerformed

